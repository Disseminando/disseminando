					<strong>	1 – Definição :</strong>
					<br><br>	
						Grupo de mulheres (mães ou não) da igreja local que oram por seus filhos, netos, sobrinhos, irmãos e líderes da Igreja.
					<br><br>	
					<strong>	2 – História :</strong>
					<br><br>
						O Projeto Ana foi elaborado no quadriênio 2006-2010, pela Secretária de Espiritualidade da Confederação Nacional Sociedade Auxiliadora Feminina – CNSFs – irmã Maria Anecy Calland Marques Serra e era Presidente, no referido quadrênio a irmã Anita Eloisa Chagas. Aqui encontrará informações a respeito: http://www.saf.org.br/projeto-ana Desenvolvemos esse trabalho na Igreja Presbiteriana em Arraial do Cabo/RJ em março de 2015 através da SAF (Sociedade Auxiliadora Feminina). Estamos expandindo em todas as igrejas da nossa região através da Federação de SAFs do Presbitério de Cabo Frio/RJ.
					<br><br>
					<strong>	3 – Objetivo :</strong>
					<br><br>
						O Projeto ANA tem por objetivo principal conscientizar, despertar e estimular mães a orarem individual e diariamente por seus filhos de oração (sanguíneos ou adotivos, netos, sobrinhos, irmãos e líderes da Igreja). Faz parte deste Projeto Ana da Igreja Presbiteriana em Arraial do Cabo o PROJETO PORTA A PORTA onde a evangelização é por meio de convites às mães não cristãs para levarem os nomes de seus filhos aos encontros, pois esse é um motivo que todas as mães necessitam: oração por seus filhos! Qual mãe não vai entregar os nomes de seus filhos para um grupo de mães que intercedem por eles ao único Deus Vivo e Verdadeiro ?
					<br><br>
					<strong>	4 – Como Participar :</strong>
					<br><br>
						A igreja local pode implantar esse projeto. Basta reunir as mães ou mulheres de oração e criar estratégias de encontros para orarem, ler a Palavra de Deus, compartilhar respostas de oração e exercer sua fé. LEIA EM ” COMO IMPLANTAR O PROJETO ANA EM MINHA IGREJA”
					<br><br>
					<strong>	5 – Ajude o projeto :</strong>
					<br><br>
						Precisamos crescer em número de mães em oração urgente! Nos ajude implantando esse projeto em sua igreja. Temos vários meios de divulgar esse projeto: camisas, banner, flyes, vídeos, Whatsapp (grupo de oração ANA), Face book, e-mails, sites, boletins dominicais, rádio, televisão, etc.
